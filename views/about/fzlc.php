    <p><span>2003年</span>——北京电视台拍摄《京城婚介专题纪录片》，面向中国大陆、港澳、美加等地对公司进行了纪实报道；</p>
    <p><span>2003年</span>——公司服务质量通过ISO 9001：2000 国际认证，成为业内首家被认证的企业；</p>
    <p><span>2004年</span>——公司在业内独家入选并荣获"北京 315 维权十佳诚信单位"称号；</p>
    <p><span>2004年</span>——应中央电视台实话实说栏目邀请，与主持人和晶一起探讨婚姻与生活；</p>
    <p><span>2005年</span>——公司创办人乔丹当选为全国婚介行业委员会副主任兼副总干事；</p>
    <p><span>2005年</span>——全国婚介行业委员会会长及上海婚介协会会长，带领全国婚介行业同仁莅临参观京城邂逅；</p>
    <p><span>2005年</span>——德国广播协会、德国广播电台记者专访；</p>
    <p><span>2005年</span>——作为中国诚信企业代表，参加人民大会堂举办的2005中国诚信企业家大会；</p>
    <p><span>2005年</span>——获得《全国婚介机构服务质量无投诉用户满意单位》；</p>
    <p><span>2005年</span>——被授予《中国企业诚信经营示范单位》；</p>
    <p><span>2006年</span>——荣获"全国婚介服务诚信示范单位"；</p>
    <p><span>2007年</span>——推出"38度服务"；</p>
    <p><span>2007年</span>——创办国际部，服务正式走出国门；</p>
