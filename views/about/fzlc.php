    <p>京城邂逅成立于1999年，是中国成立最早规模最大的婚恋咨询服务商之一。成立十年多以来，京城邂逅一直坚持以"诚信经营，成就幸福婚姻"为经营理念，公司始终处于稳健、高速发展的状态。<span>十余年来，京城邂逅已为60万对单身朋友找到了幸福的另一半。</span></p>
